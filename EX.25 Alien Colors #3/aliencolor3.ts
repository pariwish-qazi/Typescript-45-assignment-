// Using if-else chain
let alienColor: string = "green"; 

if (alienColor === "green") {
    console.log("The player earned 5 points.");
} 
alienColor = "yellow"
if (alienColor === "yellow") {
    console.log("The player earned 10 points.");
}
alienColor = "red"
if (alienColor === "red") {
    console.log("The player earned 15 points.");
} 
else {
    console.log("Alien color not Found.");
}
